# Tipos int e float.
# Int -> Numero inteiro.
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal e considerado positivo.
print(11) #int
print(-11) #int
print(0) #int
print(2024) #int

# Float -> Numero com ponto flutuante.
# O tipo float representa qualquer numero positivo ou negativo com ponto flutuante.
# Float sem sinal e considerado positivo.
print(1.1, 10.11) #float
print(0.0, -1.5) #float
print(16.17) #float

# A função type mostra o tipo que o Python inferiu ao valor.
print(type('TFabri970sw'))
print(type(1))
print(type(0.7), type(-1.1), type(0.0))
